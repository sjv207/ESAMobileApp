  <h3> Add this app to your phone</h3>
  <img src="images/smartphone/appicon.jpg">
  <p> You can create a shortcut to this web app on your smartphone home screen with a few clicks. <strong>(no
      installation
      required) </strong>. </p>
  <p>For the specific instructions on how to add a shortcut on your smartphone's home screen, please select your
    smartphone's operating system below.</p>

  <p> <strong> Please note that if you have a personal code/link, you might want to create a shortcut after you log in.
    </strong>
    This way you will be logged in automatically when you open the app with the shortcut.</p>


  <div data-role="collapsible" data-mini="true">
    <h4> Android Devices - Chrome/Firefox</h4>
    <ol>
      <li> Tap the menu icon of your browser .This looks like 3 vertical dots on the upper-right corner on Chrome,
        lower-right
        corner on Firefox: <img src="images/smartphone/chrome_menu.jpg"> </li>
      <li> Tap on <strong> Add to Home Screen </strong> </li>
      <li> Follow the instructions asking you about the name of the shortcut and confirm it.</li>
      </ul>
  </div>

  <div data-role="collapsible" data-mini="true">
    <h4> iPhone or iPad - Safari </h4>
    <ol>
      <li> Tap on <strong> Share </strong> button at the bottom of the page in Safari. This looks like a square with an
        arrow pointing out: <img src="images/smartphone/safari_share.png">
      </li>
      <li> Scroll down the list of the options. Tap on <strong> Add to Home Screen </strong>.
      <li> Follow the instructions to complete adding the shortcut. </li>
      </ul>
  </div>



